<h1>News :: Game Release - Wetspot 2</h1>
<span class="article_data">Date: 2014-11-11, Author: Surkow</span>

<h2>Pushing blocks</h2>

<p>With Wetspot 2, Dmitry Smagin<a href=#1>[1]</a> surprises us with another retro game reimplementation. Originally written in QuickBasic by Angelo Mottola<a href=#2>[2]</a>
in 1997, Dmitry Smagin created a full re-implementation of the Wetspot 2 game engine entirely in pure C and SDL. The game, an action puzzle game similar to Pengo, was considered one of the best QuickBasic games ever made. It supports 1 or 2 players, 7 enemies with different AI, tons of bonuses, music by Drozerix<a href=#3>[3]</a> and the ability to load new levels.</p>

<p>In the game you control a small crab and your target is to kill the monsters in each of the levels. The only way to accomplish this is to push bricks. When a brick hits an obstacle it releases a bonus.</p>

<p>Wetspot 2 can be freely downloaded from the GCW software repository<a href=#4>[4]</a></p>

